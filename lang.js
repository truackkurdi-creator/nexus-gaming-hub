function changeLanguage(lang) {
    if (!translations[lang]) return;
    localStorage.setItem("nexus_lang", lang);
    const t = translations[lang];

    // Change Direction and Font
    document.documentElement.dir = t.dir;
    document.documentElement.lang = lang;
    
    // If not Kurdish, change font slightly if needed or let it be
    if (lang === 'en') {
        document.body.style.fontFamily = "'Outfit', sans-serif";
    } else {
        document.body.style.fontFamily = "'Vazirmatn', 'Outfit', sans-serif";
    }

    // Translate Navbar
    document.querySelector('#nav-home').innerHTML = `<i class="fa-solid fa-house"></i> ${t["nav-home"]}`;
    document.querySelector('#nav-apps').innerHTML = `<i class="fa-brands fa-app-store-ios"></i> ${t["nav-apps"]}`;
    document.querySelector('#nav-tools').innerHTML = `<i class="fa-solid fa-toolbox"></i> ${t["nav-tools"]}`;
    document.querySelector('#nav-mods').innerHTML = `<i class="fa-solid fa-car"></i> ${t["nav-mods"]}`;
    document.querySelector('#nav-publicchat').innerHTML = `<i class="fa-regular fa-comments"></i> ${t["nav-publicchat"]}`;
    document.querySelector('#nav-aichat').innerHTML = `<i class="fa-solid fa-robot"></i> ${t["nav-aichat"]}`;
    const dashLinkObj = document.querySelector('#dashboardLink');
    if(dashLinkObj) dashLinkObj.innerHTML = `<i class="fa-solid fa-chart-pie"></i> ${t["nav-dashboard"]}`;

    const loginBtnNode = document.querySelector('#loginBtn');
    if (!window.currentUser) {
       loginBtnNode.innerHTML = `<i class="fa-solid fa-user"></i> ${t["btn-login"]}`;
    }

    // Hero Section
    const heroH1 = document.querySelector('.hero-center h1');
    if(heroH1) heroH1.innerText = t["hero-h1"];
    const heroP = document.querySelector('.hero-center p');
    if(heroP) heroP.innerText = t["hero-p"];

    const heroBtns = document.querySelectorAll('.hero-center .btn-primary');
    if(heroBtns.length >= 2) {
        heroBtns[0].innerHTML = `<i class="fa-solid fa-rocket"></i> ${t["btn-start"]}`;
        heroBtns[1].innerHTML = `<i class="fa-regular fa-comments"></i> ${t["btn-chat"]}`;
    }

    // Features Section
    const featureBoxes = document.querySelectorAll('#page-home > div:nth-of-type(2) > div');
    if(featureBoxes.length >= 3) {
        featureBoxes[0].querySelector('h3').innerText = t["feature1-title"];
        featureBoxes[0].querySelector('p').innerText = t["feature1-desc"];
        
        featureBoxes[1].querySelector('h3').innerText = t["feature2-title"];
        featureBoxes[1].querySelector('p').innerText = t["feature2-desc"];
        
        featureBoxes[2].querySelector('h3').innerText = t["feature3-title"];
        featureBoxes[2].querySelector('p').innerText = t["feature3-desc"];
    }

    // Stats
    const statBoxes = document.querySelectorAll('#page-home > div:nth-of-type(4) > div');
    if(statBoxes.length >= 4) {
        statBoxes[0].querySelector('p').innerHTML = `<i class="fa-solid fa-users" style="margin-left:5px;"></i> ${t["stat-users"]}`;
        statBoxes[1].querySelector('p').innerHTML = `<i class="fa-solid fa-layer-group" style="margin-left:5px;"></i> ${t["stat-items"]}`;
        statBoxes[2].querySelector('p').innerHTML = `<i class="fa-solid fa-cloud-arrow-down" style="margin-left:5px;"></i> ${t["stat-downloads"]}`;
        statBoxes[3].querySelector('p').innerHTML = `<i class="fa-solid fa-headset" style="margin-left:5px;"></i> ${t["stat-support"]}`;
    }

    // Auth Modals
    const loginForm = document.getElementById('loginForm');
    if (loginForm) {
        loginForm.querySelector('h2').innerHTML = `<i class="fa-solid fa-arrow-right-to-bracket"></i> ${t["login-title"]}`;
        loginForm.querySelectorAll('label')[0].innerHTML = `<i class="fa-solid fa-envelope"></i> ${t["login-email"]}`;
        loginForm.querySelectorAll('label')[1].innerHTML = `<i class="fa-solid fa-lock"></i> ${t["login-pass"]}`;
        document.getElementById('loginSubmitBtn').innerHTML = `<i class="fa-solid fa-right-to-bracket"></i> ${t["login-btn"]}`;
        const loginToggle = loginForm.querySelector('p');
        loginToggle.innerHTML = `${t["login-noacc"]} <span style="color: var(--primary-color); cursor: pointer" onclick="toggleAuth()">${t["login-register"]}</span>`;
    }

    const signupForm = document.getElementById('signupForm');
    if (signupForm) {
        signupForm.querySelector('h2').innerHTML = `<i class="fa-solid fa-user-plus"></i> ${t["signup-title"]}`;
        signupForm.querySelectorAll('label')[0].innerHTML = `<i class="fa-solid fa-user"></i> ${t["signup-name"]}`;
        signupForm.querySelectorAll('label')[1].innerHTML = `<i class="fa-solid fa-envelope"></i> ${t["login-email"]}`;
        signupForm.querySelectorAll('label')[2].innerHTML = `<i class="fa-solid fa-lock"></i> ${t["login-pass"]}`;
        document.getElementById('signupSubmitBtn').innerHTML = `<i class="fa-solid fa-user-check"></i> ${t["signup-btn"]}`;
        const signupToggle = signupForm.querySelector('p');
        signupToggle.innerHTML = `${t["signup-hasacc"]} <span style="color: var(--primary-color); cursor: pointer" onclick="toggleAuth()">${t["btn-login"]}</span>`;
    }

    // Dashboard Modal
    const dashbObj = document.getElementById('dashboardModal');
    if (dashbObj) {
        dashbObj.querySelector('h2').innerHTML = `<i class="fa-solid fa-chart-line"></i> ${t["dash-title"]}`;
        document.getElementById('tabProfile').innerHTML = `<i class="fa-solid fa-user-pen"></i> ${t["dash-tab-prof"]}`;
        document.getElementById('tabAdd').innerHTML = `<i class="fa-solid fa-cloud-arrow-up"></i> ${t["dash-tab-add"]}`;
        document.getElementById('tabRole').innerHTML = `<i class="fa-solid fa-user-shield"></i> ${t["dash-tab-role"]}`;
        
        const logoutBtn = document.getElementById('logoutBtn');
        if(logoutBtn) logoutBtn.innerHTML = `<i class="fa-solid fa-right-from-bracket"></i> ${t["btn-logout"]}`;
    }

    // Section Headings & Titles
    document.querySelector('#page-apps h2').innerHTML = `<i class="fa-brands fa-app-store-ios"></i> ${t["nav-apps"]}`;
    document.querySelector('#page-tools h2').innerHTML = `<i class="fa-solid fa-toolbox"></i> ${t["nav-tools"]}`;
    document.querySelector('#page-mods h2').innerHTML = `<i class="fa-solid fa-car"></i> ${t["nav-mods"]}`;
    document.querySelector('#page-publicchat h2').innerHTML = `<i class="fa-solid fa-users"></i> ${t["nav-publicchat"]}`;
    document.querySelector('#page-aichat h2').innerHTML = `<i class="fa-solid fa-robot"></i> ${t["nav-aichat"]}`;

    // Trending & Footer
    const latestHeading = document.querySelector('#page-home h2 i.fa-fire');
    if(latestHeading && latestHeading.parentElement) latestHeading.parentElement.innerHTML = `<i class="fa-solid fa-fire" style="color: #f97316;"></i> ${t["latest-nexus"]}`;

    const footer = document.querySelector('footer');
    if(footer) {
        const pTags = footer.querySelectorAll('p');
        if(pTags.length >= 2) {
            pTags[0].innerText = t["footer-desc"];
            pTags[1].innerText = t["footer-c"];
        }
    }
}

// Auto Load Lang on start
document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("nexus_lang") || "ku";
    changeLanguage(savedLang);
    // Also set the correct select value
    const ls = document.getElementById('langSwitcher');
    if(ls) ls.value = savedLang;
});
